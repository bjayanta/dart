/**
 * What is Encapsulation?
 * 
 * In software systems, 
 * encapsulation refers to the bundling of data with the mechanisms or, 
 * methods that operate on the data or, 
 * the limiting of direct access to some data, 
 * such as an object's components.
 */

/**
 * What is Library?
 * 
 * By default, every .dart file is a library. 
 * A library is a collection of functions and classes. 
 * A library can be imported into another library using the import keyword.
 */

/**
 * How to achieve Encapsulation in dart?
 * 
 * Encapsulation can be achieved by:
 * 1. Declaring the class properties as private by using underscore(_)
 * 2. Providing public getter and setter methods to access and update the value of private property.
 */

/**
 * Why Encapsulation is important?
 * 
 * Data Hiding: 
 * Encapsulation hides the data from the outside world. 
 * It prevents the data from being accessed by the code outside the class. 
 * This is known as data hiding.
 * 
 * Testability: 
 * Encapsulation allows you to test the class in isolation. 
 * It will enable you to test the class without testing the code outside the class.
 * 
 * Flexibility: 
 * Encapsulation allows you to change the implementation of the class without affecting the code outside the class.
 * 
 * Security: 
 * Encapsulation allows you to restrict access to the class members. 
 * It will enable you to limit access to the class members from the code outside the library.
 */