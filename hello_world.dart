void main() {
  var name = "Jayanta";

  print(name);
  print("My name is $name.");
  print("My name is " + name + ".");

  var age = 70;
  print("I am $age year's old.");
  print("I am " + age.toString() + " year's old.");

  var taka = 100.00;
  print("Amar kase \$$taka ase.");

  print(r"I am $name and $age year's old. I have $taka right now.");
}

